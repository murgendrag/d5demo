read -p "Enter a single digit number " a;

case $a in
	 0)
		echo "$a-ZERO"
	;;
		
	 1)
                echo "$a-ONE"
        ;;

	 2)
                echo "$a-TWO"
        ;;

 	 3)
                echo "$a-THREE"
        ;;

	 4)
                echo "$a-FOUR"
        ;;

 	 5)
                echo "$a-FIVE"
        ;;

	 6)
                echo "$a-SIX"
        ;;

	 7)
                echo "$a-SEVEN"
        ;;

 	 8)
                echo "$a-EIGHT"
        ;;
 	 9)
                echo "$a-NINE"
        ;;

esac
